package it.polito.tdp.PremierLeague.model;

public class MatchMigliore {

	Match m;
	Double n;
	
	public MatchMigliore(Match m, Double n) {
		super();
		this.m = m;
		this.n = n;
	}
	public Match getM() {
		return m;
	}
	public void setM(Match m) {
		this.m = m;
	}
	public Double getN() {
		return n;
	}
	public void setN(Double n) {
		this.n = n;
	}
	
	
	
}
